/**************************************************************************
* Register Macros
**************************************************************************/

/* Allows to set the CP15DISABLE FERRARI pin and thus lock the CP15 secure 
 * registers */
#define CSL_SCM_TZ_LOCKING                                      (0x0U)

/* Allows to control the DFW/SPMSIC dynamic hardware firewall for buffer 
 * overflow detection on SECURE KERNEL PRIVILEGE MODE stacks */
#define CSL_SCM_SSM_SECMEM_STATUS                               (0x4U)

/* Modena AXI Peripheral port converted by AXI2OCP into OCP protocol No burst 
 * supported No SWP or exclusive access supported No 64 Bits accesses 
 * supported */
#define CSL_SCM_PLATFORM_STATUS                                 (0xCU)

/* Activate the PAGE in internal RAM that will be taken into account by the 
 * SDP mechanism PAGE _0 is the first 4Kbyte of internal RAM PAGE_15 is the 
 * last 4Kbyte of internal RAM */
#define CSL_SCM_SDP_PAGE_ACT                                    (0x10U)

/* Teach the SSM SDP mechanism if page in internal ram are CODE or DATA PAGE 
 * _0 is the first 4Kbyte of internal RAM PAGE_15 is the last 4Kbyte of 
 * internal RAM */
#define CSL_SCM_SDP_PAGE_RD                                     (0x14U)

/* Status if pages have been hit by write accesses PAGE _0 is the first 4Kbyte 
 * of internal RAM PAGE_15 is the last 4Kbyte of internal RAM */
#define CSL_SCM_SDP_PAGE_WR                                     (0x18U)

/* Secure Demand Paging Page LRU list 5 */
#define CSL_SCM_SDP_LRU_LIST_5                                  (0x1CU)

/* Secure Demand Paging Page LRU list 4 */
#define CSL_SCM_SDP_LRU_LIST_4                                  (0x20U)

/* Secure Demand Paging Page LRU list 3 */
#define CSL_SCM_SDP_LRU_LIST_3                                  (0x24U)

/* Secure Demand Paging Page LRU list 2 */
