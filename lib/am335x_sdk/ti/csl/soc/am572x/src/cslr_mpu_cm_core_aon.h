/**************************************************************************
* Register Macros
**************************************************************************/

/* This register enables the MPU domain power state transition. It controls
 * the HW supervised domain power state transition between ON-ACTIVE and
 * ON-INACTIVE states. It also hold one status bit per clock input of the
 * domain. */
#define CSL_MPU_CM_CORE_AON_CM_MPU_CLKSTCTRL_REG                (0x0U)
/* Below define for backward compatibility */
#define CM_MPU_CLKSTCTRL  (CSL_MPU_CM_CORE_AON_CM_MPU_CLKSTCTRL_REG)


/* This register controls the static domain depedencies from MPU domain
 * towards 'target' domains. It is relevant only for domain having system
 * initiator(s). */
#define CSL_MPU_CM_CORE_AON_CM_MPU_STATICDEP_REG                (0x4U)
/* Below define for backward compatibility */
#define CM_MPU_STATICDEP  (CSL_MPU_CM_CORE_AON_CM_MPU_STATICDEP_REG)


/* This register controls the dynamic domain depedencies from MPU domain
 * towards 'target' domains. It is relevant only for domain having OCP master
 * port(s). */
#define CSL_MPU_CM_CORE_AON_CM_MPU_DYNAMICDEP_REG               (0x8U)
/* Below define for backward compatibility */
#define CM_MPU_DYNAMICDEP  (CSL_MPU_CM_CORE_AON_CM_MPU_DYNAMICDEP_REG)


/* This register manages the MPU clocks. */
#define CSL_MPU_CM_CORE_AON_CM_MPU_MPU_CLKCTRL_REG              (0x20U)
/* Below define for backward compatibility */
#define CM_MPU_MPU_CLKCTRL  (CSL_MPU_CM_CORE_AON_CM_MPU_MPU_CLKCTRL_REG)


/* This register manages the MPU_MPU_DBG clocks. [warm reset insensitive] */
#define CSL_MPU_CM_CORE_AON_CM_MPU_MPU_MPU_DBG_CLKCTRL_REG      (0x28U)
/* Below define for backward compatibility */
#define CM_MPU_MPU_MPU_DBG_CLKCTRL  (CSL_MPU_CM_CORE_AON_CM_MPU_MPU_MPU_DBG_CLKCTRL_REG)



