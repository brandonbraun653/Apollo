/* This register controls the MPU domain power state to reach upon a domain
 * sleep transition. If the value programmed in this register correspond to a
 * lower power state than the one programmed in MPU-SS for CPU0 and/or CPU1,
 * then value of this register is overwritten in PRCM logic to limit the power
 * state to enter. Notes: - Even if value of this register is overwitten in
 * PRCM logic, value of this register remains unchanged. - If user programs
 * MPU power domain to go to CSWRET, then he can not program L2$ to OFF mode. */
#define CSL_MPU_PRM_PM_MPU_PWRSTCTRL_REG                        (0x0U)
/* Below define for backward compatibility */
#define PM_MPU_PWRSTCTRL          (CSL_MPU_PRM_PM_MPU_PWRSTCTRL_REG)


/* This register provides a status on the MPU domain current power state.
 * [warm reset insensitive] */
#define CSL_MPU_PRM_PM_MPU_PWRSTST_REG                          (0x4U)
/* Below define for backward compatibility */
#define PM_MPU_PWRSTST            (CSL_MPU_PRM_PM_MPU_PWRSTST_REG)


/* This register contains dedicated MPU context statuses. [warm reset
 * insensitive] */
#define CSL_MPU_PRM_RM_MPU_MPU_CONTEXT_REG                      (0x24U)
/* Below define for backward compatibility */
#define RM_MPU_MPU_CONTEXT        (CSL_MPU_PRM_RM_MPU_MPU_CONTEXT_REG)



