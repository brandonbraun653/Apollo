/* WP3 mask register */
#define CSL_OCPWPNOCC1_MASTERIDMASK3                            (0x4CU)

/* DMAFILTER */
#define CSL_OCPWPNOCC1_DMAFILTER                                (0x50U)

/* Selects the MReqInfo to be mapped to the STP message MReqInfo field The
 * user is restricted to trace a minimum of 8*MReqInfo Bit MReqInfo MReqInfo
 * Select 15 Reserved 1111 14 MReqDMAChannelID[4] 1110 13 MReqDMAChannelID[3]
 * 1101 12 MReqDMAChannelID[2] 1100 11 MReqDMAChannelID[1] 1011 10
 * MReqDMAChannelID[0] 1010 9 Reserved 1001 8 MReqDomain[2] 1000 7
 * MReqDomain[1] 0111 6 MReqDomain[0] 0110 5 Reserved 0101 4 MReqCachable 0100
 * 3 MReqSupervisor 0011 2 MReqSecure 0010 1 MReqType[1] 0001 0 MReqType[0]
 * 0000 */
#define CSL_OCPWPNOCC1_MREQINFOSELECT                           (0x54U)

/* Data bytes select register */
#define CSL_OCPWPNOCC1_DATABYTESELECT                           (0x58U)

/* System trace compression control register */
#define CSL_OCPWPNOCC1_COMPCTLREG                               (0x5CU)

/* EMU1 Trigger Out is activated when target address is outside the
 * watch-point address register [1:0] address range. */
#define CSL_OCPWPNOCC1_TRIGGERCTL                               (0x60U)

/* System event detection enable register. */
#define CSL_OCPWPNOCC1_SYSEVTDETECTEN                           (0x64U)

/* System events sampling window register */
#define CSL_OCPWPNOCC1_SYSEVTWINDOW                             (0x68U)


