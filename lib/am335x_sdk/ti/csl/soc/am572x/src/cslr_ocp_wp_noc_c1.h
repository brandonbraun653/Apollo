/**************************************************************************
* Register Macros
**************************************************************************/

/* This register contains the IP revision code */
#define CSL_OCPWPNOCC1_REVISION                                 (0x0U)

/* This register controls the various parameters of the OCP interface */
#define CSL_OCPWPNOCC1_SYSCONFIG                                (0x10U)

/* This register provides status information about the module, excluding the
 * interrupt status information */
#define CSL_OCPWPNOCC1_SYSSTS                                   (0x14U)

/* This register holds functional configuration of the module. */
#define CSL_OCPWPNOCC1_CFG                                      (0x24U)

/* This register defines the top address when the OCP-WP is configured for
 * address range. */
#define CSL_OCPWPNOCC1_ADDR0                                    (0x28U)

/* This register defines the bottom address when the OCP-WP is configured for
 * address range */
#define CSL_OCPWPNOCC1_ADDR1                                    (0x2CU)

/* WP0 filtering register Debug Port data are captured when WP0 is enabled and
 * all the unmasked MReqInfo & MCmd match */
#define CSL_OCPWPNOCC1_MASTERIDMATCH0                           (0x30U)

/* WP0 mask register */
#define CSL_OCPWPNOCC1_MASTERIDMASK0                            (0x34U)

/* WP1 filtering register Debug Port data are captured when WP1 is enabled and
 * all the unmasked MReqInfo & MCmd match */
#define CSL_OCPWPNOCC1_MASTERIDMATCH1                           (0x38U)

/* WP1 mask register */
#define CSL_OCPWPNOCC1_MASTERIDMASK1                            (0x3CU)

/* WP2 filtering register Debug Port data are captured when WP2 is enabled and
 * all the unmasked MReqInfo & MCmd match */
