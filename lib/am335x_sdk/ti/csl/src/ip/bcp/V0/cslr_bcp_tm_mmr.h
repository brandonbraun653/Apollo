    volatile Uint32 BCP_DLG_HW_TIMESTAMP; /* BCP Datalogger Hardware Timestamp */
    volatile Uint32 BCP_DLG_HW_TIMESTAMP_CTRL; /* BCP Datalogger Hardware Timestamp ctrl */
    volatile Uint32 BCP_DLG_HOLD_RST; /* BCP DLG hold rst register */
    volatile Uint32 TM_CONTROL; /* TM control register */
    volatile Uint32 TM_TX_CDMAHP_READ_PRIORITY; /* TM TX CDMAHP read priority register */
    volatile Uint32 TM_TX_QFIFO_RD_DEST_SEL; /* TM TX QFIFO read dest sel and arbitrtion priority register */
    volatile Uint32 TM_RX_QFIFO_WR_ARB_PRIORITY_3_0; /* TM RX QFIFO write arb priority register for channels 0 to 3 */
    volatile Uint32 TM_RX_QFIFO_WR_ARB_PRIORITY_7_4; /* TM RX QFIFO write arb priority register for channels 7 to 4 */
