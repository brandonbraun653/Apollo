    volatile Uint32 BCP_EOI; /* BCP EOI register */
    volatile Uint32 CDMA_DESC_STARVE_STATUS; /* CDMAHP Descriptor Starve status register */
    volatile Uint32 CDMA_DESC_STARVE_CLEAR; /* CDMAHP Descriptor Starve Clear register */
    volatile Uint32 CDMA_DESC_STARVE_SET; /* CDMAHP Descriptor Starve Set register */
    volatile Uint32 CDMA_DESC_STARVE_INTR_SEL; /* CDMAHP Descriptor Starve intr sel register */
