/* Controls halting debug. Usage constraints • The effect of modifying the
 * C_STEP or C_MASKINTS bit when the system is running with halting debug
 * enabled is UNPREDICTABLE. Halting debug is enabled when C_DEBUGEN is set to
 * 1. The system is running when S_HALT is set to 0. • When C_DEBUGEN is set
 * to 0, the processor ignores the values of all other bits in this register. */
#define CSL_PMMCSCS_DHCSR                                       (0xDF0U)

/* The DCRSR characteristics are: Purpose With the DCRDR, see Debug Core
 * Register Data Register, DCRDR on page C1-836, the DCRSR provides debug
 * access to the ARM core registers, special-purpose registers, and
 * Floating-point extension registers. A write to DCRSR specifies the register
 * to transfer, whether the transfer is a read or a write, and starts the
 * transfer. Usage constraints Only accessible in Debug state */
#define CSL_PMMCSCS_DCRSR                                       (0xDF4U)

/* The DCRDR characteristics are: Purpose • With the DCRSR, see Debug Core
 * Register Selector Register, DCRSR on page C1-834, the DCRDR provides debug
 * access to the ARM core registers, special-purpose registers, and
 * Floating-point extension registers. The DCRDR is the data register for
 * these accesses. • Used on its own, the DCRDR provides a message passing
 * resource between an external debugger and a debug agent running on the
 * processor. Note The architecture does not define any handshaking mechanism
 * for this use of DCRDR. Usage constraints See Use of DCRSR and DCRDR on page
 * C1-837 for constraints that apply to particular transfers using the DCRSR
 * and DCRDR. */
#define CSL_PMMCSCS_DCRDR                                       (0xDF8U)

/* Purpose Manages vector catch behavior and DebugMonitor handling when
 * debugging. Usage constraints • Bits [23:16] provide DebugMonitor exception
 * control. • Bits [15:0] provide Debug state, halting debug, control. */
#define CSL_PMMCSCS_DEMCR                                       (0xDFCU)

