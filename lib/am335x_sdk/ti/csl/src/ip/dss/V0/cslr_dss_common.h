/**************************************************************************
* Register Macros
**************************************************************************/

/* This register contains the IP revision code */
#define CSL_DSSCOMMON_DISPC_REVISION                            (0x0U)

/* This register allows to control various parameters of the OCP interface. */
#define CSL_DSSCOMMON_DISPC_SYSCONFIG                           (0x4U)

/* This register provides status information about the module, excluding the 
 * interrupt status information. */
#define CSL_DSSCOMMON_DISPC_SYSSTATUS                           (0x8U)

/* End Of Interrupt number (for H08 interrupt) */
#define CSL_DSSCOMMON_DISPC_IRQ_EOI                             (0x20U)

/* Per-end of group (31 down to 0) internal signaling raw interrupt status 
 * vector, line #0. Raw status is set even if end of group (31 down to 0) 
 * interrupt is not enabled. Write 1 to set the (raw) status, mostly for 
 * debug. */
#define CSL_DSSCOMMON_DISPC_IRQSTATUS_RAW                       (0x24U)

/* Per-end of group (31 down to 0) internal signaling "enabled" interrupt 
 * status vector, line #0. Enabled status isn't set unless event is enabled. 
 * Write 1 to clear the status after interrupt has been serviced (raw status 
 * gets cleared, i.e. even if not enabled). */
#define CSL_DSSCOMMON_DISPC_IRQSTATUS                           (0x28U)

/* Per-end of group (31 down to 0) internal event interrupt enable bit vector, 
 * line #0. Write 1 to set (enable interrupt). Readout equal to corresponding 
 * _CLR register. */
#define CSL_DSSCOMMON_DISPC_IRQENABLE_SET                       (0x2CU)

/* DISPC_IRQENABLE_CLR */
#define CSL_DSSCOMMON_DISPC_IRQENABLE_CLR                       (0x30U)

/* IRQ wake up register */
#define CSL_DSSCOMMON_DISPC_IRQWAKEEN                           (0x34U)

/* MFLAG control register */
#define CSL_DSSCOMMON_DISPC_GLOBAL_MFLAG_ATTRIBUTE              (0x40U)

/* The register configures the DMA buffers allocations to the pipeline. */
#define CSL_DSSCOMMON_DISPC_GLOBAL_BUFFER                       (0x44U)

/* DISPC_BA0_FLIPIMMEDIATE_EN */
#define CSL_DSSCOMMON_DISPC_BA0_FLIPIMMEDIATE_EN                (0x48U)

/* DISPC debug bus control register */
#define CSL_DSSCOMMON_DISPC_DBG_CONTROL                         (0x4CU)

/* DISPC debug status register */
#define CSL_DSSCOMMON_DISPC_DBG_STATUS                          (0x50U)

/* Register to control clock gating at DISPC sub-module level */
#define CSL_DSSCOMMON_DISPC_CLKGATING_DISABLE                   (0x54U)


