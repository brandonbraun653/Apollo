    volatile Uint32 RX_TEST_2;
    volatile Uint32 TX_TEST_CHRG_DET;
    volatile Uint32 CHRG_DET;
    volatile Uint32 PWR_CNTL;
    volatile Uint32 UTMI_INTERFACE_CNTL_1;
    volatile Uint32 UTMI_INTERFACE_CNTL_2;
    volatile Uint32 BIST;
    volatile Uint32 BIST_CRC;
    volatile Uint32 CDR_BIST2;
    volatile Uint32 GPIO;
    volatile Uint32 USB2PHYCM_TRIM;
    volatile Uint32 USB2PHYCM_CONFIG;
    volatile Uint32 USBOTG;
    volatile Uint32 AD_INTERFACE_REG1;
    volatile Uint32 AD_INTERFACE_REG2;
    volatile Uint32 AD_INTERFACE_REG3;
    volatile Uint32 ANA_CONFIG1;
    volatile Uint32 ANA_CONFIG2;
